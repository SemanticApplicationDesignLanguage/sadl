package com.ge.research.sadl.ui.tests;

import static com.ge.research.sadl.utils.ResourceManager.OWLDIR;
import static com.ge.research.sadl.utils.ResourceManager.OWLFILEEXTWITHPREFIX;

import java.nio.file.Paths;

import com.ge.research.sadl.ui.tests.AbstractSadlPlatformTest.GeneratedOutputLocationProvider;

/**
 * Enumeration of the supported output formats that are generated by the
 * application.
 * 
 * @author akos.kitta
 */
public enum GeneratedOutputFormat implements GeneratedOutputLocationProvider {

	/**
	 * The OWL format.
	 */
	OWL {

		@Override
		public String get(String inputFilePath) {
			return OWLDIR + "/" + extractFileName(inputFilePath) + OWLFILEEXTWITHPREFIX;
		}

	},

	/**
	 * The rules output.
	 */
	RULES {

		@Override
		public String get(String inputFilePath) {
			return OWLDIR + "/" + extractFileName(inputFilePath) + ".rules";
		}

	};

	protected String extractFileName(String inputFilePath) {
		return Paths.get(inputFilePath).getFileName().toString().replaceFirst("[.][^.]+$", "");
	}

}
