//		ValueRow singleRow = valueTable.getRow();
		EList<ValueRow> valueRows = valueTable.getRows();
		
		List<List<Node>> rows = new ArrayList<List<Node>>(valueRows != null ? valueRows.size() : 1);
//		if (singleRow != null) {
//			List<Node> row = translate(singleRow);
//			rows.add(row);
